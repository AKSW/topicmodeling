package org.aksw.simba.topicmodeling.concurrent.tasks;

import java.lang.Thread.State;

public class TaskState {

    public Task task;
    public State state;
    public StackTraceElement stackTrace[];
    public Throwable throwable;

    public TaskState(Task task, State state, StackTraceElement[] stackTrace) {
        this.task = task;
        this.state = state;
        this.stackTrace = stackTrace;
    }

    public TaskState(Task task, State state, Throwable throwable) {
        this.task = task;
        this.state = state;
        this.throwable = throwable;
    }

    public TaskState(Task task, State state) {
        this.task = task;
        this.state = state;
    }

    public Task getTask() {
        return task;
    }

    public void setTask(Task task) {
        this.task = task;
    }

    public State getState() {
        return state;
    }

    public void setState(State state) {
        this.state = state;
    }

    public StackTraceElement[] getStackTrace() {
        return stackTrace;
    }

    public void setStackTrace(StackTraceElement[] stackTrace) {
        this.stackTrace = stackTrace;
    }

    public Throwable getThrowable() {
        return throwable;
    }

    public void setThrowable(Throwable throwable) {
        this.throwable = throwable;
    }
}
